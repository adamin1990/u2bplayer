package com.ijiaban.u2bplayer.bean;

public class Category {
	
	/**
	 * 类别名称
	 */
	public String name;
	/**
	 * 查询时根据的标识
	 */
	public String sname;
	
	
	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	} 
}
